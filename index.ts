// prettier-ignore
function replace_unroll<R>(data: any[], replaceValue: any, aggregator:R[] = []):R[] {
    if (data.length === 0) return aggregator;
    if (data.length === 1) return [replaceValue, ...aggregator];
    let [_, __, ...tail] = data;
    return replace_unroll(tail, replaceValue, [replaceValue, replaceValue, ...aggregator]);
}

function replace_unroll_as_loop<R>(data: any[], replaceValue: R): R[] {
  const aggregator: R[] = [];
  for (let i = 0; i < data.length; i += 2) {
    data[i] ?? aggregator.push(replaceValue);
    data[i + 1] ?? aggregator.push(replaceValue);
  }
  return aggregator;
}

type Replace_TailRecursion_Unroll<
  T extends any[],
  ReplaceValue,
  Agg extends any[] = []
> = T extends [any, any, ...infer Tail]
  ? Replace_TailRecursion_Unroll<Tail, [ReplaceValue, ReplaceValue, ...Agg]>
  : T["length"] extends 1
  ? [ReplaceValue, ...Agg]
  : Agg;

type Test_Replace_TailRecursion_Mutli = Replace_TailRecursion_Unroll<
  [...Zeros_Length_999, ...Zeros_Length_999],
  1
>;

