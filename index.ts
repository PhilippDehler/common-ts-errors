// Naive approach
function replace_naive<R>(data: any[], replaceValue: R): R[] {
  if (data.length === 0) return [];
  const [head, ...tail] = data;
  return [replaceValue, ...replace_naive(tail, replaceValue)];
}

function replace_naive_as_loop<R>(data: any[], replaceValue: R): R[] {
  const aggregator: R[] = [];
  for (let i = 0; i < data.length; i++) aggregator.push(replaceValue);
  return aggregator;
}

type Replace_Naive<T extends any[], ReplaceValue> = T extends [
  any,
  ...infer Tail
]
  ? [ReplaceValue, ...Replace_Naive<Tail, ReplaceValue>]
  : [];
//@ts-expect-error
type Test_Replace_Naive = Replace_Naive<Zeros_Length_999, 1>; // Type instantiation is excessively deep and possibly infinite.

// prettier-ignore
function replace_tailRecursion<R>(data: any[], replaceValue: R, aggregator:R[] = []): R[] {
    if (data.length === 0) return aggregator;
    const [_, ...tail] = data;
    return replace_tailRecursion(tail, replaceValue, [replaceValue, ...aggregator]);
}

// same as  replace_naive_as_loop
// Optimization Step One Tail Recursion
type Replace_TailRecursion<
  T extends any[],
  ReplaceValue,
  Agg extends any[] = []
> = T extends [any, ...infer Tail]
  ? Replace_TailRecursion<Tail, [ReplaceValue, ...Agg]>
  : Agg;

